import math

#menghitung akar kudrat
print(math.sqrt(25))

#menghitung pangkat
print(math.pow(5,3))

#menghitung nilai absolut(mutlak)
print(math.fabs(-6))

#menghitung faktorial
print(math.factorial(5))

#mengembalikan nilai pembulatan ke atas
print(math.ceil(5.2))

#mengembalikan nilai pembulatan ke bawah
print(math.floor(5.7))


#menghitung sinus (dalam radian)
print(math.sin(math.radians(40)))

#menghitung kosinus (dalam radian)
print(math.cos(math.radians(40)))

#menghitung tangen (dalam radian)
print(math.tan(math.radians(30)))
